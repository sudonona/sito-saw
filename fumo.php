      <h1><b>Danni Causati dal Fumo :</b></h1>

      <p>Malattie respiratorie, infarti e cardiopatie gravi. Questi e molti altri sono i danni del fumo. Smettere è l’unica soluzione per restare in salute.</p><br>
      
      <h2>Quali danni provoca il fumo ?</h2>

      <p> Il vizio del fumo è uno dei principali fattori che provocano problemi di salute con conseguenze dannose, e nello specifico le patologie correlate riguardano:</p>
      <p><b><i>Malattie cardio-circolatorie :</b></i> il fumo riduce il trasporto di ossigeno ai tessuti e fa aumentare la pressione arteriosa e la frequenza cardiaca. Questo danneggia cuore e arterie, favorendo lo sviluppo di malattie cardiovascolari come aritmie, infarto al miocardio, cardiopatia ischemica e ictus;</p>
      <p><b><i>Patologie respiratorie e infezioni broncopolmonari :</b></i> le sostanze tossiche del fumo una volta assorbite dai polmoni possono favorire l’insorgenza di asma, BPCO, enfisema, apnee ostruttive, insufficienza respiratoria, fibrosi polmonare e tumore al polmone.</p>
      <p>Ma non solo. Gli effetti nocivi del fumo tendono ad accumularsi nel tempo e provocare altresì:<br>
      <p><b><i>Invecchiamento della pelle :</b></i> fumare causa il rilascio nell’organismo di radicali liberi che ostacolano la produzione di collagene e comportano perdita di elasticità, comparsa di macchie cutanee e la formazione di rughe soprattutto nella regione intorno alle labbra;</p>
      <p><b><i>Effetti sul cavo orale :</b></i> diminuisce le difese immunitarie nei confronti della placca batterica favorendo l’ingiallimento della dentina, gengiviti, alitosi e l'insorgenza del cancro alla bocca;</p>
      <p><b><i>Danni sulla sessualità maschile :</b></i> la sigaretta avendo un effetto dannoso a livello vascolare, è un fattore di rischio importantissimo nello sviluppo della disfunzione erettile;</p>
      <p><b><i>Problemi in gravidanza :</b></i> il fumo influisce negativamente sull'apparato riproduttivo femminile, aumenta il rischio di aborti e provoca menopause più precoci.</p>
      Le sostanze nocive presenti nella sigaretta (nicotina, catrame, monossido di carbonio, ammoniaca, ecc.) danneggiano l'organismo con diverse azioni:<br>
      <p><b><i>Cancerogena</b></i> (sviluppo e accrescimento del cancro del polmone, del cavo orale, della laringe, dell'esofago, del pancreas, della vescica e del rene);</p>
      <p><b><i>Irritante </b></i>(tosse, bronchite cronica, enfisema);</p>
      <p><b><i>Ossidante</b></i>(infarto cardiaco, ictus cerebrale, emorragia cerebrale, trombosi, invecchiamento precoce). </p>
      <p>Non esiste quindi una soglia di sicurezza sotto la quale il fumo non produce danni..</p><br>
      
      <h2><b>I danni del fumo passivo</b></h2>

      <p>Attenzione perché anche il fumo passivo è considerato irritante e cancerogeno per l'uomo. Infatti, è ben nota la correlazione tra il fumo passivo e l'insorgenza di malattie cardiovascolari e di cancro nei non fumatori.</p>
      <p>L’esposizione passiva al tabacco fumato da altri e, in generale, frequentare ambienti inquinati, è responsabile di 1/4 delle malattie respiratorie e particolarmente dannoso soprattutto per neonati e bambini.</p><br>
     
      <h2><b>I vantaggi dello smettere di fumare</b></h2>

        <p>Smettere di fumare è certamente uno dei passi più importanti che si possono fare per ridurre l'insorgenza di malattie e allungare la speranza di vita. Infatti:</p>
        <p>La pressione del sangue e la frequenza del battito cardiaco tornano normali e il rischio di avere un infarto cardiaco si riduce del 50% dopo 1 anno, mentre il rischio di ictus cerebrale regredisce in 5-10 anni dall’astensione;</p>
        <p>Il rischio di cancro del polmone è paragonabile a quello di chi non ha mai fumato dopo 10 anni dalla sospensione.
        Inoltre, smettere di fumare migliora la qualità della vita:</p>
        <p> Olfatto e gusto migliorano già dopo alcuni giorni;</p>
        <p> La pelle ritorna più luminosa dopo alcune settimane;</p>
        <p> I denti diventano più bianchi;</p>
        <p> L'alito diventa più gradevole;</p>
        <p> Gli abiti e i capelli non hanno più l'odore di fumo;</p>
        <p> Il respiro migliora e scompare la tosse da fumo;</p>
        <p> Si salgono più facilmente le scale;</p>
        <p> Ci si sente meglio risparmiando anche soldi.</p>
