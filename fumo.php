      <h1><b>Come smettere di fumare: metodi e tecniche</b></h1>

      <p>Il primo strumento per smettere di fumare? La forza di volontà ma, se non dovesse essere sufficiente, ecco una serie di consigli per dire no al fumo.</p>

      <p>La vita è una resistenza continua all’inerzia che tenta di sabotare il nostro volere più profondo. Chi si stanca di volere, vuole il nulla”, scriveva il filosofo tedesco Friedrich Nietzsche. Una frase che calza a pennello a chi, con grande forza di volontà, sta cercando di <b>smettere di fumare</b>, trovandosi - ed è inevitabile - ad affrontare un vero e proprio momento di cambiamento.</p>
      <p><b>Un passaggio contraddistinto da due fasi: una di preparazione, una di azione.</b> Volere è potere, recita il detto. Ma non è così semplice, perché quando si è abituati a fumare si tende a perdere di vista i vantaggi della vita senza sigarette e diventa difficile tornare indietro. A lungo andare, il rischio è di perdere la motivazione per liberarsi dal tabagismo, dimenticando che <b>in realtà non esiste alcuna buona ragione per fumare.</b></p>
