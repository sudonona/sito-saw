      <p><b>Sono centinaia, infatti, le sostanze tossiche rilasciate quando si accende una sigaretta.</b> Fumare rappresenta una delle prime cause evitabili di morte prematura: questo è un dato di fatto, così come gli effetti negativi del fumo sulla salute. Secondo i dati pubblicati dall’Organizzazione Mondiale della Sanità, nel mondo il tabacco è responsabile di un numero maggiore di vittime rispetto a quelle provocate da alcol, droghe, AIDS, incidenti stradali, omicidi e suicidi messi insieme.</p>
      <p>Si calcola che ogni anno, a livello mondiale, <b>si potrebbero evitare circa 6 milioni di morti soltanto abolendo l’uso del tabacco</b>; in Italia si stima che le morti ascrivibili al fumo vadano da 70.000 a 83.000 ogni anno. Va ricordato, inoltre, che oltre un quarto delle vittime del fumo ha un’età compresa tra i 35 e i 65 anni.</p>

      <br><h2><b>I consigli per smettere di fumare</b></h2>

      <p>Ad ogni modo, come si è deciso di fumare si può decidere di smettere. Le statistiche riportano che abbandonare le sigarette non è un obiettivo facile da raggiungere, ma a disposizione di chi fa questa scelta - attualmente - ci sono una serie di strumenti sui quali si può fare affidamento. Di seguito una serie di consigli per smettere di fumare:</p>
      <p><b><i>Counselling:</i></b> numerosi studi dimostrano che il solo consiglio di smettere di fumare, da parte del proprio medico, fa in modo che un determinato numero di persone lo segua. Talvolta i fumatori possono partecipare a gruppi di auto-mutuo-aiuto per smettere di fumare.</p>
      <p><b><i>Centri antifumo:</i></b> in Italia sono presenti centinaia di centri antifumo (tutte le informazioni sono sul sito dell’Istituto Superiore di Sanità) che, per aiutare a smettere di fumare, ricorrono a vari strumenti: counselling individuale e trattamento di gruppo per smettere di fumare, ma anche prescrizione di prodotti sostitutivi della nicotina oppure di farmaci per la disassuefazione.</p>
      <p><b><i>Farmaci:</i></b> in alcuni casi il medico, accanto al supporto psicologico e all’aiuto comportamentale, può prescrivere antidepressivi oppure altri farmaci in grado di aiutare il paziente a superare il momento più complicato, quello del distacco dalla sigaretta.</p>
      <p><b><i>Trattamenti sostitutivi:</i></b>cerotti, chewing gum e altri prodotti a base di nicotina. Cercare di smettere di fumare equivale a confrontarsi con la dipendenza psicologica da tabacco che si esprime (anche) nel piacere e nella gestualità del fumare, in particolar modo attraverso disturbi causati dall’astinenza da nicotina, una sostanza che il corpo è avvezzo ad assumere.</p>
      <p><b><i>Sigarette elettroniche:</i></b> in merito ai rischi e benefici delle sigarette elettroniche (alcuni anni fa si è registrato un vero e proprio boom di vendite di sigarette elettroniche), si è discusso molto. Gli studi condotti fino a questo momento non consentono di escludere possibili danni legati a questo nuovo modo di fumare.</p>
      <p><b><i>Libri per smettere di fumare:</i></b> gli scaffali delle librerie sono pieni di manuali per smettere di fumare. In alcuni casi la lettura di questi volumi può aiutare concretamente, o quantomeno stimolare il desiderio di smettere di fumare in chi non ci pensava proprio.</p>
      <p>Va da sé che ancora oggi la forza di volontà rimane il primo, indispensabile “strumento” per dire addio al fumo e alle sigarette. Certo, la nicotina produce dipendenza, ed è normale ricadere nel vizio del fumo dopo qualche giorno (oppure settimana) di astinenza dal fumo. In questi casi ciò che conta è non scoraggiarsi, ritentare finché non si riesce a smettere, magari facendosi aiutare a prendere questa difficile decisione una volta per tutte e a realizzarla in concreto.</p>
